# Basic Examples of the different basic data structure in Python

#List
list_type = ["john", 78, True, 68.9] 

#Tuples
tuples_type = ("gift", 78, True, 68.9) 

#Dictionary
dict_type = {
    "key":"value",
    12952:8242884,
    0.983:0.82458,
    False:True
}

#Sets
set_type = {"Austin", 78, True, 68.9}

print(type(list_type))
print(type(tuples_type))
print(type(dict_type))
print(type(set_type))